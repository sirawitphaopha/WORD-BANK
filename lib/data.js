// แปลง SEED เป็น array ของ object { text, category_id, novel, meaning }
export function parseSeed() {
  return SEED.trim().split('\n').map((ln) => {
    const p = ln.trim().split('|');
    const catIdx = parseInt((p[1] || '8').trim(), 10) || 0;
    const novelIdx = parseInt((p[2] || '0').trim(), 10) || 0;
    return {
      text: p[0].trim(),
      category_id: 'c' + catIdx,
      novel: NOVELS[novelIdx] || NOVELS[0],
      meaning: (p[3] || '').trim(),
    };
  });
}
